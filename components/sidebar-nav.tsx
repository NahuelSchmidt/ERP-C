function NavLink({ item }: { item: NavItem }) {
  const pathname = usePathname()
  const isActive = pathname === item.href || pathname.startsWith(item.href + "/")

  return (
    <Link
      href={item.href}
      className={
        isActive
          ? "flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium bg-white text-[#0a0a0a]"
          : "flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium text-white/50 hover:text-white hover:bg-white/[0.07] transition-all group"
      }
    >
      <span className={isActive ? "text-[#0a0a0a]" : "text-white/30 group-hover:text-white/70 transition-colors"}>
        {item.icon}
      </span>
      {item.label}
    </Link>
  )
}

